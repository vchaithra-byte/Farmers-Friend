<p>Minor land smoothening before sowing helps in better in-situ moisture conservation. Seeds are very small and take 5-7 days to germinate. Hence good seeds, land preparation helps in better germination, minimize weeds problem and effective soil moisture conservation.</p>
 </p><br/>
 <p>In Uttaranchal where frequent ploughing operations are difficult to carry out effective digging and turning of soil, removing perennial weeds, land smoothening, providing inward slope with a shallow drain helps in taking out excess rain water. </p></br>
 
 
  <h3 id="method">Seed rate</h3>
 <p>
A seed rate of 10 kg ha-1 is found to be optimum for drill sowing and 5 kg ha-1 for raising seedlings for transplanted condition.<p>
</p>
</p>
 </p>
    
	 <h3 id="Sowing">Sowing/Planting</h3>
 <p>
 Line sowing is beneficial helps in inter cultivation and control of weeds effectively. Maintenance of optimum plant population of 4-5 lakh plants ha-1 this is attained by line sowing using seed drill giving a spacing of 22.5-30 cm between rows and 7.5-10 cm between plants Transplanting in irrigated condition
 </p>
 
 <h3 id="Spacing">Spacing and Manuring</h3>
 <center><img src="http://agrigoaexpert.res.in/icar/category/agriculture/fieldcrops/millet/ragi/2.jpg" width="480" height="370" /> </center>
 <p> Entire P2O5 and K2O are to be applied at sowing, whereas nitrogen is to be applied in two or three split doses depending upon moisture availability. 
In areas of good rainfall and moisture availability:  50% of recommended nitrogen is to be applied at sowing and the remaining 50% in two equal splits at 25-30 and 40-45 days after sowing. 
In areas of uncertain rainfall: 50% at sowing and the remaining 50% around 35 days after sowing is recommended. 
Adequate application of FYM, 7.5 to 10 tonnes per hectare help in better development of root growth.

 </p>
 <h3 id="Bio-fertilizers">Bio-fertilizers</h3>
 <p>
Treating seeds with Azospirillumbrasilense(N fixing bacterium) and Aspergillusawamori(P Solubilizing fungus) @ 25 g/kg seed is beneficial. In case seeds are to be treated with seed dressing chemicals, treat the seeds first with seed dressing chemicals and then with bio-fertilizers at the time of sowing. </p>
<P>
 <B>Procedure for inoculating seeds with bio-fertilizers  </B></BR>
1.	Bio-fertilizer culture specific to the crop is to be used @ 25g per kg of seed.</BR>
2.	Sticker solution is necessary for effective seed inoculation. This can be prepared by dissolving 25 g jaggery or sugar in 250 ml water and boiling for 5 minutes. The   solution thus prepared is cooled.</BR>
3.	Smear the seeds well using the required quantity of sticker solution. Then add culture to the seeds and mix thoroughly so as to get a fine coating of culture on the seed.</BR>
4.	The culture-coated seed is to be dried well in shade to avoid clumping of seeds.</BR>
5.	 Use the inoculated seeds for sowing.</BR></BR>
</P>

