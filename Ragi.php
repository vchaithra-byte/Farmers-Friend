 <p>
a)	Summer ploughing or ploughing after the harvest of previous crop</BR>
b)	Ploughing across the slope </BR>
c)	Errection of small section bunds at an interval of 10-12m depending up on the slope  </BR>
and levelling the depressions  </BR>
d)	Opening a dead furrow at 3.3 to 4.0 m interval.</BR> </BR>
