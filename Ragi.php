<h3 id="intro">Introduction</h3>
 <p>  		
Finger millet is important small millet grown in India. It is a staple food in many hilly regions of the country. It is grown both for grain and forage. Grains are rich in minerals and are the richest source of calcium used in many preparations like cakes, puddings, sweet etc. it is also a rich source of iron, protein, fiber and other minerals and is a gluten (elastic texture) free food. It has low fat content and contains mainly unsaturated fat. Due to rich fiber content finger millet is believed to be a good laxative and prevents constipation. It is a good food for people who suffering from diabetes, liver diseases, high blood pressure, heart weakness and asthma. Its green straw is suitable for making silage. The major finger millet growing states are Karnataka, Uttarakhand, Maharashtra, Tamil Nadu, Odisha, Andhra Pradesh and Gujarat.</p>
 <h3 id="cul">Climate </h3>
 <p>
Finger millet is a crop of tropical and subtropical climate and can be cultivated up to an altitude of 2100 m. It is heat loving plant and for its germination, the minimum temperature required is 8-10°C. A mean temperature range of 26-29°C is the best for proper development and good crop yield. </p>
 <h3 id="Soil">Soil</h3>
 <p>
 Finger millet can be grown on a wide adaptability to different soil from very poor to very fertile and can tolerate a certain degree of alkalinity. The best soil is alluvial, loamy and sandy soil with good drainage.
 </p>

