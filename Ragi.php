</p>

<p><u>Cropping sequence</u></br>
<b>Northern Bihar:</b> Potato-paddy-finger millet cropping sequence is highly 
remunerative than other cropping sequences for garden land. <br/>

<b>Southern Karnataka or Deccan plateau: </b>Finger millet-potato-maize or finger 
millet-onion-finger millet are highly remunerative cropping 
sequences. <br/>
<b>Assured rainfall areas:</b> raising crop of cowpea or green gram or Sesamum
followed by sowing / transplanting of early duration finger 
millet can be practiced.
 </p>
 
<h3 id="Weed">Weed control</h3>
<p>
Weeds problem in ragi crop can be effectively managed by cultural and mechanical measures<br/>
<b>Line sowing:</b> 2-3 inter cultivations and one hand weeding <br/>
<b>Broadcast crop: </b>2 effective hand weeding will minimize weeds<br/>
<b>In assured rainfall and irrigated areas:</b> Pre-emergence spray: Isoproturon @ 0.5 kg a.i./ha. (Rainfed areas), Oxyflurofen @ 0.1 lta.i /ha (Irrigated areas)<br/>
<b>Post-emergent spray: </b>2, 4-D sodium salt @ 0.75 kg a.i./ha Spraying around 20-25 days after sowing effectively control weeds. <br/>
</p>


<h3 id="Irrigation">Irrigation</h3>
<p>
Depending on soil type, weather condition and duration of variety, 8-14 irrigations are necessary.<br/>
<b>Light soils:</b> Irrigate the crop once in 6-8 days <br/>
<b>Heavy soils: </b> once in 12-15 days.<br/>
</p>

 <h3 id="Harvesting">Harvesting  </h3>
 <p>
The crop matures in about 95 to 110dasy for early varieties and 115 to 125 days for medium to late duration varieties depending on the tract and the variety. The ear heads are harvested with ordinary sickles and straw is cut close to ground. At some places under rainfed condition, the whole plant with ear head is cut, heaped and then threshed<p>
