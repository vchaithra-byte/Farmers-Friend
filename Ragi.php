 <h3 id="Nursery">Nursery management</h3>
 <p>
An area of 150 m2 is required to raise seedlings to suffice 1 ha.35-38 beds of 4 feet wide and 25 feet long and 4 inches height fine seed bed is required. <BR/>
Apply 2-3 baskets of well decomposed FYM along with 1 kg super phosphate, half kg muriate of potash and half kg ammonium phosphate and 750 g zinc sulphate per bed. Sow the seeds by opening rows at every 3 inch uniformly. Cover the seed with well decomposed FYM and soil/sand/water every bed. Top dress with urea 500 g per bed when the seedlings are 12-14 days old.<BR/>
Seedlings of 21-25 days old are ideal for transplanting in rows of 22.5-25 cm with 2 seedlings /hill with 10 cm between hills.<BR/>
Treating seeds with Azospirullumbrasilense (nitrogen fixing bacterium) and Aspergillusawamouri (phosphate solubalizing fungus) @ 25 g kg-1 is beneficial.<BR/>

 </p>
 
