 <h3 id="Yield">Yield  </h3>
 <p>
It is possible to harvest 25-30 qt/ha of grain and 60-70 qt/ha of fodder. The straw of finger millet makes nutritious fodder. It can be conserved by putting up in well-built stak.</p>



