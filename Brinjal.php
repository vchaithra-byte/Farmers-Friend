    <h3 id="fer">Inter-Culture and Weed Control in Brinjal Farming  </h3>
    <p> The weeds should be controlled as soon as they seen, either by traditional method of hand weeding and hoeing or by application of herbicides. Regular or frequent shallow cultivation should be done at regular intervals so as to keep the field free from weeds and to facilitate soil aeration and proper root development.
The most serious weed in brinjal is the Orabanchae sp. It is root parasite and it should be controlled carefully .Gap filling should be done wherever needed during evening hours followed by irrigation .Pre- plant soil incorporation of Fluchloralin (1- 1.5 kg/ha) or Oxadiazon  (0.5 kg/ha) and pre-planting surface spraying of Alachlor (1-1.5 kg/ha) control the weeds of brinjal successfully.
</p>
   <h3 id="fer">Application of manures and fertilizers </h3>
   <p>Apply 2 kg each of Azospirillum and Phosphobacteria in the main field at planting.<br\>
<b>Varieties :-</b> Basal dose : FYM 25 t/ha, NPK 50:50:30 kg/ ha. Top dressing : 50 kg N/ha on 30th day of planting or during earthing up.
<br\>
<b>Hybrids :- </b>Basal dose : FYM 25 t/ha, NPK 100:150:100 kg/ha. Top dressing : 100 kg N/ha on 30th day of planting or during earthing up.

</p>
   <h3 id="pest">Pests and Diseases </h3>
   <center><table border="1" style="border-collapse:collapse;"><tr>
    <td>
    <b>Mosaic(transmitted by aphids)</b></td>
    <td>Mosaic mottling of the leaves and stunting of plant.The mosaic symptoms in early stages are mild but later it takes severe forms.The infected leaves are deformed,small and leathery,causing 30-40% losses in yield.</td>
    <td>Removal and destruction of infected plants and eradication of susceptible weed hosts.In the nursey,aphids can be controlled by applcation of carbofuran(1kg a.i/ha)in the nursery bed at the time of sowing seeds followed by 2-3 foliar sprays of Phosphamidon (0.05%)at an interval of 10 days.</td>
  </tr>
  <tr>
    <td><b>Little leaf of brinjal(transmitted by leaf hoppers)</b></td>
    <td>Infected leaves are thinner than healthy leaves.All the freshly produced flowers become phylloid,leaves become progressively smaller,the petals very often turn green and appear leaf like.The disease causes 100% losses in yield.</td>
    <td>Dip seedlings in 0.2% carbofuran (75% WP) for 24 hr reduces vector population.The diseased plants must be removed from the field.</td>
  </tr>
  </table></center>
