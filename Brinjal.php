<h3 id="intro">Introduction</h3> 
 <p>Brinjal or Eggplant is an important crop of sub tropics and tropics. The name brinjal is popular in Indian subcontinents and is derived from Arabic and Sanskrit whereas the name eggplant has been derived from the shape of the fruit of some varieties, which are white and
resemble in shape to chicken eggs. It is also called aubergine (French word) in Europe. The brinjal is of much importance in the warm areas of Far East, being grown extensively in India, Bangladesh, Pakistan, China and the Philippines. It is also popular in Egypt, France, Italy and United States. In India, it is one of the most common, popular and principal vegetable crops
grown throughout the country except higher altitudes. It is a versatile crop adapted to different agro-climatic regions and can be grown throughout the year. It is a perennial but grown commercially as an annual crop. A number of cultivars are grown in India, consumer preference being dependent upon fruit colour, size and shape.
</p>
