   <h3 id="har">Harvesting and Yield</h3>
   <p>The brinjal fruits are harvested when they attain full size and colour but before start of ripening. Tenderness bright colour and glossy appearance of fruit is the optimum stage of harvesting of fruits. When the vegetables look dull, it is an indication of maturity and loss of quality in brinjal farming.</p>
