   <h3 id="cli">Climatic Condition </h3>
   <p>The brinjal is a warm season crop, therefore susceptible to severe frost. Low temperature during the cool season causes deformation of vegetables. A long and warm growing season is desirable for successful brinjal farming. Cool nights and short summers are not suitable for satisfactory production. A daily average temperature of 13 to 21 C is most favourable for optimum growth and yield. The brinjal seed germinate well @25C</p>
   <h3 id="suit">Suitable Soil </h3>
   <p>The brinjal plants can be grown in all types of soil varying from light sandy to heavy clay. Well drained soil rich in organic matter with pH of 6.5- 7.5. Light soils are good for an early yield, while clay-loam and silt-loam are well suited for higher yield..
 </p>
 <h3 id="pro">Propagation of Soil For Brinjal Farming</h3>
<p>Since the crop remain in the field for a number of months. The soil should be thoroughly prepared by ploughing 4 to 5 times before transplanting the seedlings. Bulky organic manures like well rotten crowding or compost should be incorporated evenly on the soil. Thoroughly prepare the field with the addition of FYM @ 25 t / ha and form ridges and furrows at a spacing of 60 cm. Apply 2 kg / ha of Azospirillum and 2 kg / ha of Phosphobacteria by mixing with 50 kg of FYM. Irrigate the furrows and transplant 30-35 days old seedlings at 60 cm apart on the ridges.</p>
