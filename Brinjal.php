   <h3 id="rat1"> Seed Rate & Sowing Time</h3>
   <p> Varieties : 400 grams /ha ,Hybrids : 200 grams /ha.December – January and May – June.</p>
  <h3 id="sow"> Seed treatment in Brinjal Farming  </h3>
  <p> Treat the brinjal seeds with Trichoderma viride @ 4 g / kg or Pseudomonas fluorescens @ 10 g / kg of seed. Treat the seeds with Azospirillum @ 40 g / 400 g of seeds using rice gruel as adhesive. Irrigate with rose can. In raised nursery beds, sow the seeds in lines at 10 cm apart and cover with sand. Transplant the seedlings 30 – 35 days after sowing at 60 cm apart in the ridges.</p>
   <h3 id="irr">Transplanting in Brinjal Farming  </h3>
   <p> The seedlings are ready in 4-5 weeks for transplanting, when they attained a height of 12-15 cm with 3-to 4leavess. Harden the seedlings by withholding irrigation. Uproot the seedlings carefully without injury to the roots. Transplanting should b e done during evening hours followed by irrigation. Firmly press the soil around the seedlings. Spacing depends upon the fertility status of soil, type of verities and suitability of the season. In general 60×60 cm spacing is kept for non-spreading type
verities and 75-90×60-75 cm for spreading type verities
</p>
