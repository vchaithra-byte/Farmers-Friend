   <h3 id="irr">Irrigation/Water supply </h3>
   <p>Water the field as per the need of crop. Timely irrigation is quite essential for good growth, flowering, fruit setting and development of fruits. Higher yield may be obtained at optimum moisture level and soil fertility conditions. In plains irrigation should be applied every third to fourth day during hot weather and every 7 to12 days during winter. Irrigation is given before top dressing of there is no rain. The brinjal field should be regularly irrigated to keep the soil moist during frosty days.</p>
