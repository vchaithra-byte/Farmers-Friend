    <h1><a name="top"></a>TROPICAL &amp; SUB-TROPICAL FRUITS : Grapes</h1>
    
    <table CELLSPACING="0" BORDER="0" CELLPADDING="2">
      <tr>
        <td VALIGN="top"><h2>1. Introduction :</h2></td>
      </tr>
      <tr>
        <td VALIGN="top"><p align="justify"><font face="Sans serif, verdana, arial" size="2">The
        grape is the most important crop grown in the world. Mostly it grown for making wines and
        preparation of raisin and then as a table fresh fruit. While in India, it is mainly grown
        for table use. Grape cultivation is believed to have originated near Caspian Sea, however,
        Indians know grapes since Roman times. Total area under grapes in India is about 40,000
        ha, distributed mainly in Maharashtra, Karnataka, Andhra Pradesh and Tamil Nadu.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>2. Economic importance:</h2>
        <p><font face="Sans serif, verdana, arial" size="2">At present, grape is
        the most important fruit crop grown commercially with the objectives.</font></p>
        <p><font face="Sans serif, verdana, arial" size="2">a.For table purpose</font></p>
        <p><font face="Sans serif, verdana, arial" size="2">b.For export purpose</font></p>
        <p><font face="Sans serif, verdana, arial" size="2">c.For making wines and</font></p>
        <p><font face="Sans serif, verdana, arial" size="2">d.For making raisins.</font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Fresh grapes are a
        fairly good source of minerals like calcium, phosphorous, iron and vitamins like B. Famous
        champagne and other desert wines are prepared from grapes.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>3. Climatic Requirements:</h2><p style="text-align: justify"><font face="Sans serif, verdana, arial"
        size="2">The ideal climate for grape growing is the Mediterranean climate. In its natural
        habitat, the vines grow and produce during the hot and dry period. Under South Indian
        conditions &#150; vines produce vegetative growth during the period from April to
        September and then fruiting period from October to March. Temperatures above 10<sup>0</sup>C
        to 40<sup>0</sup>C influence the yield and quality. High humidity and cloudy weather
        invite many fungal diseases, besides lowering the T.S.S. : Acid ratio.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>4. Soil Preference: </h2><p style="text-align: justify"><font face="Sans serif, verdana, arial"
        size="2">The grape is widely adopted to various soil conditions, but the yield and quality
        reach to the highest on good fertile soils have pH 6.5 to 8.5, organic carbon above 1.0%,
        free of lime and having a medium water holding capacity. Early but medium yields with high
        T.S.S. are harvested on medium type of soils.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>5. Varieties:</h2><p><font
        face="Sans serif, verdana, arial" size="2">a . Table purpose varieties- </font><ol
        TYPE="i">
          <ol TYPE="i">
            <li><font face="Sans serif, verdana, arial" size="2">Seeded varieties &#150;Cardinal,
              concord Emperor, Italia, Anab-e-shahi, Cheema sahebi, Kalisahebi, Rao Sahebi,</font></li>
            <li><font face="Sans serif, verdana, arial" size="2">Seedless varieties &#150; Thompson
              seedless, flame seedless, kishmish chorni, perlette, Arkavati.</font></li>
          </ol>
        </ol>
        <ol START="2" TYPE="a">
          <li><font face="Sans serif, verdana, arial" size="2">Raisin purpose varieties &#150;
            Thompson seedless, manik chaman, sonaka, Black corinth, Black monukka, Arkavati, Dattier</font></li>
          <li><font face="Sans serif, verdana, arial" size="2">Wine varieties &#150; Chardonnay,
            Cabernet Saurignnon, Bangalore Blue, Muscat, Blanc, Pinot Noir, Pinot Blane, White
            Riesling, and Merlot.</font></li>
        </ol>
        </td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>6. Propagation: </h2>
        <p style="text-align: justify"><font face="Sans serif, verdana, arial" size="2">Grapevine
        is most commonly propagated by hard-wood cuttings, though propagation by seed soft wood
        cutting, layering, grafting and budding is specific to certain situations. Occasionally,
        unrooted cuttings are also planted directly in the field in the pre-determined position
        for a vine. For hardwood cuttings, IBA, 1000 ppm treatment is useful for early, better and
        uniform rooting of cutting. For grafting Dog ridge, Ramsey, 1616, 1613,1103P, So4, etc.
        are used. Sometimes the rootstocks are planted in the field and there they are grafted
        with suitable varieties.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>7. Planting and Season:</h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Usually planting is
        done from October onwards till January. Rarely planting is also done during June-July
        where the monsoon is late. Monsoon planting is avoided mainly for avoiding diseases on
        young growth. For planting N-S direction the trenches are opened. The size of trench may
        be 60 to 75 cm. Deep wide. Then these trenches are filled with FYM, organic manures,
        5:10:5 organic mixtures, single super phosphates, biofertilisers, neem cakes, etc. Spacing
        for planting is maintained depending on soil type, variety and method of training. The
        distance between two rows may be 2 to 3 m while distance between vines within a row will
        be half of that, accommodating vines from 2000 to 5000 per hectare.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>8. Interculturing: </h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">The following aspects
        are important:-</font></p>
        <ol TYPE="i">
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Gap filling: To be
            done preferably during one month after planting.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Recut: Basal cut
            keeping 2/3 buds is taken one month after planting with an objective to get uniform new
            growth.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Supporting : The
            bamboo supports are fixed for vine support and young growing points are trained on them</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Weeding: The vine
            rows are weeded out, twice/thrice depending on the intensity of weeds.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Irrigations &#150;
            regular depending on soil and season are given.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Fertilizers are
            applied with cow during slurry to hasten the growth.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Suitable plant
            protection measures are following depending on incidence of pests and diseases.</font></p>
          </li>
        </ol>
        </td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>9. Care of young orchard:</h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Grapes vines takes
        about 1.5 to 2 years after planting to bear the first crop. During this period the care of
        young vines is taken as under:-</font></p>
        <ol TYPE="i">
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Training: The vines
            are trained first on bamboo and then on support &#150; trellis. A suitable method of
            training is adopted.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Pruning &#150;
            Initial pruning is done only for training i.e. for developing trunk, arm, fruiting, canes,
            etc.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">The fertilizer doses
            &#150; including organic, inorganic and bio-fertilizers are applied twice in a year.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Plant protection
            schedule is prepared and followed for the total initial period of growing.</font></p>
          </li>
        </ol>
        </td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>10. Special Horticultural practices:</h2>
        <ol TYPE="i">
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Pruning and
            training: The vines are trained on a suitable trellis i.e. &#145;T&#146;, &#145;Y&#146;,
            &#145;H&#146; or bower and regularly pruned twice in a year. First annual pruning is done
            during the month of April to get the new vegetative growth while second pruning to get the
            crop is done during the month of October. While doing April pruning 0 to 2 buds on arm are
            kept while doing October pruning 5 to 10 buds on fruiting cane are kept. Use of HCN is
            done to have early, uniform and higher sprouting particularly after winter pruning is
            made.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Girdling: Vines are
            trunk girdled at bloom period to increase the fruit set, to increase the weight and T.S.S.
            and also to enhance maturity.</font></p>
          </li>
          <li><p align="justify"><font face="Sans serif, verdana, arial" size="2">Use of hormones: The
            following plant hormones at various stages and concentrations are usually used to increase
            the yield and to improve the quality of bunches.</font></p>
          </li>
        </ol>
        <ol TYPE="A">
          <li><font face="Sans serif, verdana, arial" size="2">At bloom&#133;&#133;&#133;&#133;.GA3..
            &#133;20 to 30 ppm; CCC &#133;500 ppm</font></li>
          <li><font face="Sans serif, verdana, arial" size="2">At setting&#133;&#133;&#133;&#133;GA3
            &#133;.30 to 40 ppm; 6BA&#133;.. 5-10 ppm</font></li>
          <li><font face="Sans serif, verdana, arial" size="2">At 4-6 mm size &#133;.GA3 &#133; 40 to
            50 ppm; Brassinos...100 ppm</font></li>
          <li><font face="Sans serif, verdana, arial" size="2">At 6-8 mm size &#133; GA3 &#133;30 to
            50 ppm; Cppu&#133;2-3 ppm</font></li>
        </ol>
        </td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>11. Irrigation:</h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Grape is strictly
        irrigated perennial crop and regularly irrigated. For flood irrigation, 5-7 days during
        summer 8-10 days during winter and 15-20 days during rainy season &#150; interval is
        maintained while for drip irrigation, 40-50 L; 30-40, 20-30 L of water per day per vine,
        water is applied.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>12. Nutrition:</h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Balanced nutrition and
        use of chemical, organic and biofertilisers is essential to get a good crop of good
        quality every year. About 700 to 900 N, 400 to 600 P and 750 to 1000 K Kgs/ ha/year are
        applied to get about 30 to 35 tonnes produced yearly.</font></p>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">The use of vermiphos,
        biomeal, mixtures of 5:10:5 ormichem, micronutrient mixtures have proved useful in grape
        production. Fertilizers are applied mainly twice in a year at the time of pruning, besides
        occasional foliar sprays are also practiced. Now-a days, Fertigation techniques is being
        popular in grape growers.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>13. Plant protection:</h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Grape shoots, leaves,
        blossoms and berries are attacked by many fungi and insect pests, besides some nematodes
        are also cause damage to roots.</font></p>
        <u><p ALIGN="JUSTIFY"></u><font face="Sans serif, verdana, arial" size="2">Major fungal
        diseases &#150; Anthracnose, Powdery mildew, Downy mildew, </font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Dead arm, Botraytis
        and Botrodiplodia.</font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Major bacterial
        diseases &#150;Xyantomonas, blight.</font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Viral diseases &#150;
        Fan leaf disease</font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Major Insect pests
        &#150; Flea beetle, Mealy bug, Red mite, Thrips, caterpillars.</font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Soil born pests &#150;
        Nematodes, phyloxere, white ants, and white grub.</font></p>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Control measures: A
        number of systemic and contact fungicides and pesticides are available and are to be used
        as per following local schedule.</font></p>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Besides pests and
        diseases the crop is to be protected against weeds, Cyprus, doob grass, Parthenium olerace
        are some of the common and important weeds found in vineyards. They are controlled by
        frequent weeding fruit ching/growing cover crops or by using chemical weedicides as
        Gramaxone, Basaline, Roundup, Glycel, etc.</font></p>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Grape bunches are also
        to be protected against hot sun, Cold wave, dry air spell, Dew and Storm. Some chemical
        some physiological and some mechanical means methods are adopted.</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>14. Harvesting and yields: </h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Normal grape harvest
        season starts in February and continuous up to end of April. Well matured bunches having
        at least 18<sup>0</sup> Brix are harvested </font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">Av. yields - For
        seedless varieties - 20 to 30 t/ha/y</font></p>
        <p ALIGN="JUSTIFY"><font face="Sans serif, verdana, arial" size="2">For seeded varieties -
        40 to 50 t/ha/y</font></td>
      </tr>
      <tr>
        <td VALIGN="top"><h2>15. Post Harvest handling:</h2>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Harvested grapes are
        packed in 2 to 4 kg-corrugated boxes. Grape guards, pouches are kept inside the boxes for
        distant markets. Pre-cooling and use of grape guards are the musts for cold storage and
        export markets.</font></p>
        <p align="justify"><font face="Sans serif, verdana, arial" size="2">Mumbai, Delhi,
        Calcutta, Ahmedabad, Ludhiana, Patna, Jamshedpur, Bangalore, Hyderabad, are the main
        market places in the country.</font></td>
      </tr>
