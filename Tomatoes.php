        <h2><a name="Harvesting"></a>Harvesting Time and Method</h2>
      <p >The harvest of tomatoes is dependent on the closeness
      to the market. Stages of tomato harvesting: -</p>
      <ul>
        <li><font face="Verdana" size="2" color="#FF0000">Mature green fruit:</font><font face="Verdana" size="2" color="#008000">- Fruits are bulk packed in ventilated containers
          for shipment. Those picked to be shipped are picked at the mature
          green stage and sprayed with ethylene 48 hours prior to shipping.
          Immature green tomatoes will ripen poorly and be of low quality.
          However, tomatoes harvested at the mature green stage will ripen into
          a product indiscernible from vine-ripened fruit. A simple way to
          determine maturity is to slice the tomato with a sharp knife. If seeds
          are cut, the fruit is too immature for harvest and will not ripen
          properly.</font></li>
        <li><font face="Verdana" size="2" color="#FF0000">Pink colour fruits :</font><font face="Verdana" size="2" color="#008000">- For Local sale of tomatoes may be vine ripened
          to a firm ripe or a full red color before harvesting.</font></li>
        <li><font face="Verdana" size="2" color="#FF0000">Vine-ripened tomatoes</font><font face="Verdana" size="2" color="#008000"> should be harvested at the breaker stage to
          ensure the best quality. Fruit at the breaker stage, which have some
          interlocular gel and a pinkish red color on the inside, are sure to be
          mature. Such fruit can be handled and shipped better than that which
          has more color, and it will often bring a higher price than less
          mature tomatoes. If tomatoes are to be vine-ripened, fields should be
          harvested often and thoroughly to narrow the range of ripeness.</font></li>
        <li><font face="Verdana" size="2" color="#FF0000">Fully ripe:</font><font face="Verdana" size="2" color="#008000">- Processing tomatoes are picked fully ripe.</font></li>
      </ul>
    <p ALIGN="JUSTIFY"><font face="Verdana" size="2" color="#008000">Harvesting every day may be desirable during the peak
      of the season. Remove all diseased, misshapen, and otherwise cull tomatoes
      from the vines as soon as they are discovered. Remove discarded tomatoes
      from the field to avoid the spread and buildup of diseases and insect
      pests.</font></p></div>
