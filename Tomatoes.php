        <h2><a name="climate"></a> Climatic Requirements</h2>
        <p>Tomato is a warm season crop, it requires warm and
        cool climate. The plants cannot withstand frost and high humidity. Also
        light intensity affects pigmentation, fruit colour, fruit set. The plant
        is highly affected by adverse climatic conditions. It requires different
        climatic range for seed germination, seedling growth, flower and fruit
        set, and fruit quality. Temperature below 10<sup>0</sup>C and above 38<sup>0</sup>C
        adversely affects plant tissues thereby slow down physiological
        activities. It thrives well in temperature 10<sup>0</sup>C to 30<sup>0</sup>C
        with optimum range of temperature is 21-24<sup>0</sup>C. The mean
        temperature below 16<sup>0</sup>C and above 27<sup>0</sup>C are not
        desirable. The plant doesnâ€™t withstand frost, it requires low to
        medium rainfall, and does well under average monthly temperature of 21
        to 23<sup>0</sup>C. Avoid water stress and long dry period as it causes
        cracking of fruits. Bright sunshine at the time of fruit set helps to
        develop dark red coloured fruits.</p>
