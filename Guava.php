    <td VALIGN="top"><font FACE="Arial" SIZE="2"><p ALIGN="JUSTIFY"
    style="text-align: justify"></font><font face="sans serif, verdana, arial" size="2">The
    Guava, <i>Psidium guajawa</i> is one of the important fruit crops grown in India. It is
    originated in tropical America, from Mexico to Peru and today is commercially grown in
    South Asia, Hawaiin Islands Sri Lanka, Cuba, Myanmar and India. In India, the guava is
    grown in Uttar Pradesh, Bihar, Madhya Pradesh and Maharashtra.</font></p>
    <p ALIGN="JUSTIFY" style="text-align: justify"><font face="sans serif, verdana, arial"
    size="2">The guava is a very hardy tree and it is grown with a less inputs. It is rich
    source of vitamin C, pectin and minerals. The fruits are also used to make products like
    jam, jelly, cheese, juice, nectar, etc. The tree lives longer and bears profusely under
    wide climatic and soil conditions, easily and with less expenses.</font></td>
