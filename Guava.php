    variety of climatic conditions. It favours distinct winters and tolerates high rainfall
    and drought conditions. Fruit setting and fruit development are poor if the temperatures
    are high and humidity is low.</font></p>
    <p ALIGN="JUSTIFY" style="text-align: justify"><font face="sans serif, verdana, arial"
