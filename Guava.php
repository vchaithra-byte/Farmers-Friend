    <td VALIGN="TOP"><font FACE="Arial" SIZE="2"><p ALIGN="JUSTIFY"
    style="text-align: justify"></font><font face="sans serif, verdana, arial" size="2">Training
    and pruning : The guava plants are trained to a single stem. No branching is allowed upto
    the height of 1 m on main trunk. Pruning to young plants is necessary to have uniform and
    well balanced growth and shape. Judicious pruning is also recommended prior to flowering
    of selected bahar. Pruning increase the productivity and makes the various operations like
    spraying, harvesting easy.</font></p>
    <p ALIGN="JUSTIFY" style="text-align: justify"><font face="sans serif, verdana, arial"
    size="2">Bahar treatment: This consists of withholding of water prior to flowering of
    bahar. Normally there are three bahars i.e. Mrig bahar, Hast bahar and Amba bahar, out of
    which only one is selected depending on climate and market. Root pruning and shoot pruning
    are also followed as a supporting treatments at bahar treatment. </font></p>
    <p ALIGN="JUSTIFY" style="text-align: justify"><font face="sans serif, verdana, arial"
    size="2">Bending of branches: This practice is only followed in certain erect growing
    types to improve flowering.</font></td>
