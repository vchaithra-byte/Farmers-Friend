    style="text-align: justify"></font><font face="sans serif, verdana, arial" size="2">The
    amount of fertilisers and manures depend on soil, variety, yield potential and age of the
    tree. The guava responses well to organise and biofertilisers and balanced NPK. Many times
    deficiencies of Mg, Z &amp; Mn are seen which can be rectified by spraying with cheleted
    micronutrients or micronutrients mixtures. The fertilizers schedule is modified by
    adopting periodical soil and tissue analysis data. Well grown trees require 360g nitrogen,
    180g phosphorus and potash/tree/year, in addition to 60 to 100kg of organic matter. Excess
    of chemical N should be avoided to get a good quality.</font></td>
